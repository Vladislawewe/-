.ORIG x3000       ; Начало программы

LD  R1, NUM1       ; Загружаем значение NUM1 (3) в регистр R1
LD  R2, NUM2       ; Загружаем значение NUM2 (2) в регистр R2

ADD  R3, R1, R2     ; Складываем R1 и R2, результат записываем в R3 (3 + 2 = 5)

NOT  R4, R2       ; Инвертируем значение R2, записываем в R4 (-2 - 1 = -3)
ADD  R4, R4, #1     ; Прибавляем 1 к R4, чтобы получить -2 
ADD  R4, R1, R4     ; Складываем R1 и R4, записываем в R4 (3 - 2 = 1)

AND  R5, R5, #0     ; Обнуляем регистр R5

LOOP  AND  R6, R2, #0   ; Обнуляем R6
  ADD  R6, R6, R1   ; Копируем значение R1 в R6
  ADD  R5, R5, R6   ; Складываем R5 и R6 (R5 = R5 + R1)
  ADD  R2, R2, #-1   ; Уменьшаем R2 на 1 (R2 = R2 - 1)
  BRp  LOOP          ; Если R2 положительный, переходим к LOOP

HALT              ; Завершаем выполнение программы

NUM1  .FILL #3      ; Определение константы NUM1 = 3
NUM2  .FILL #2      ; Определение константы NUM2 = 2

.END              ; Конец программы

